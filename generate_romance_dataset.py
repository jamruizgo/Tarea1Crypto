seen = set()
filtered = []

for lang, text in raw_phrases:
    t = text.strip()
    if 50 <= len(t) <= 100 and t not in seen:
        seen.add(t)
        filtered.append((lang, t))

print(f"Total frases após filtro: {len(filtered)}")

if len(filtered) < 500:
    print(f"AVISO: apenas {len(filtered)} frases passaram o filtro de 50-100 chars.")

# Tomar las primeras 500
final = filtered[:500]

out_path = "romance_dataset.csv"
with open(out_path, "w", newline="", encoding="utf-8") as f:
    writer = csv.writer(f, quoting=csv.QUOTE_ALL)
    writer.writerow(["id", "language", "text", "char_count"])
    for i, (lang, text) in enumerate(final, 1):
        writer.writerow([i, lang, text, len(text)])

